class Gallery extends Component {
  state = {activeTabId: imagesList[0].id}

  updateActiveId = id => {
    this.setState({activeTabId: id})
  }

  render() {
    const {activeTabId} = this.state
    const filteredImage = imagesList.find(each => each.id === activeTabId)
    const {imageUrl, imageAltText} = filteredImage
    return (
      <div className="container">
        <div className="inner-container">
          <div className="image-container">
            <img src={imageUrl} className="image" alt={imageAltText} />
          </div>

          <div className="thumbnail-container">
            <h1 className="heading">Nature Photography</h1>
            <p className="description">Nature Photography by Rahul</p>
            <ul className="thumbnails-container">
              {imagesList.map(each => (
                <ThumbnailItem
                  itemDetails={each}
                  key={each.id}
                  isActive={each.id === activeTabId}
                  clickTabItem={this.updateActiveId}
                />
              ))}
            </ul>
          </div>
        </div>
      </div>
    )
  }
}
